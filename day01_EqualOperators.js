/*

==:     does not care about data types
===:    does care about data types

not equal operators
!=:     does not care about data types
!==:    does care about data types


*/

console.log(9=="9"); // true
console.log(9=="10"); //false

console.log(9==="9"); //false
console.log(9===9);

console.log(9!="9"); //false
console.log(9 !=="9"); //true

